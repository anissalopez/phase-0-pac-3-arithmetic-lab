function add(a,b){
    return a += b;

}
function subtract(a,b){
    return a -= b;

}
function multiply(a,b){
    return a *= b

}
function divide(a,b){
    return a /= b;

}
function makeInt(string) {
    let result = parseInt(string, 10);
    return result;
}
function preserveDecimal(string){
    let result = parseFloat(string);
    return result;
}
function increment(n){
    return n+=1
}
function decrement(n){
    return n-=1
}